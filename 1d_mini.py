def Rule90(s):
    if s == [1,1,0] or s == [1,0,0] or s == [0,1,1] or s == [0,0,1]:
        return 1
    else:
        return 0

def Rule110(s):
    if s == [1,1,0] or s == [1,0,1] or s == [0,1,1] or s == [0,1,0] or s == [0,0,1]:
        return 1
    else:
        return 0


# #Задаём функцию отрисовки: на вход массивы размер массива и т.д.
def paint(p, a, Size):
    for x in range(Size):
        if a[x] == 1:
            p[x] = '#'
        elif a[x] == 0:
            p[x] = ' '
        print(p[x], end='')
    print('')

