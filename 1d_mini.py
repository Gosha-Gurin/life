#Далее несколько функций заполнения массива
def END(a, SIZE):
    a = [0] * (SIZE-2)
    a.append(1)
    a.append(0)
    return a

def MID(a, SIZE):
    a = [0] * (SIZE//2)
    a.append(1)
    a.append(1)
    for x in range(SIZE//2):
        a.append(0)
    return a

def RAND(a, SIZE):
    for x in range(SIZE):
        a.append(rand.randint(0, 1))
    return a

